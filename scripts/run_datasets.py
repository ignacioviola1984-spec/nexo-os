"""Run the full pipeline over several synthetic datasets and write docs/EVIDENCE.md.

For each seed it regenerates an independent synthetic dataset (into a temp store),
runs one full orchestrator cycle headless, and records the row counts + the number
of HITL actions proposed + the run status. This is evidence that the system runs
deterministically and robustly across *different* datasets - not just one curated
fixture - without touching the committed store or any real data.

Reproduce:  python scripts/run_datasets.py
"""

from __future__ import annotations

import json
import os
import subprocess
import sys
import tempfile
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
SEEDS = [20260630, 1001, 2002, 3003]  # the default seed + three alternates
DOMAIN_COLS = ["clientes", "polizas", "cuotas", "comisiones", "leads", "cotizaciones"]


def _run(args: list[str], env: dict[str, str]) -> list[dict]:
    """Run `python -m nexo_os <args>` and return the parsed JSON log lines."""
    proc = subprocess.run(
        [sys.executable, "-m", "nexo_os", *args],
        cwd=REPO_ROOT,
        env=env,
        capture_output=True,
        text=True,
    )
    out = proc.stdout + "\n" + proc.stderr
    events = []
    for line in out.splitlines():
        line = line.strip()
        if line.startswith("{"):
            try:
                events.append(json.loads(line))
            except json.JSONDecodeError:
                pass
    if proc.returncode != 0:
        raise RuntimeError(f"`nexo_os {' '.join(args)}` failed:\n{out}")
    return events


def run_seed(seed: int, workdir: Path) -> dict:
    env = dict(os.environ)
    env.update(
        NEXO_SYNTHETIC_SEED=str(seed),
        NEXO_SYNTHETIC_DB_PATH=str(workdir / "synthetic.duckdb"),
        NEXO_RUNTIME_DB_PATH=str(workdir / "runtime.duckdb"),
    )
    seed_events = _run(["seed"], env)
    counts = next(e["counts"] for e in seed_events if e.get("event") == "seed.complete")
    run_events = _run(["orchestrate"], env)
    finished = next(e for e in run_events if e.get("event") == "run.finished")
    return {
        "seed": seed,
        "counts": counts,
        "acciones": finished["acciones"],
        "estado": finished["estado"],
    }


def render_md(results: list[dict]) -> str:
    header = "| seed | " + " | ".join(DOMAIN_COLS) + " | acciones | estado |"
    sep = "|" + "---|" * (len(DOMAIN_COLS) + 3)
    lines = [header, sep]
    for r in results:
        cells = [str(r["counts"].get(c, "")) for c in DOMAIN_COLS]
        flag = "default" if r["seed"] == SEEDS[0] else ""
        lines.append(
            f"| {r['seed']}{' (' + flag + ')' if flag else ''} | "
            + " | ".join(cells)
            + f" | {r['acciones']} | {r['estado']} |"
        )
    table = "\n".join(lines)
    return f"""# Evidence — multiple synthetic datasets

Generated by `python scripts/run_datasets.py`. Each row is an **independent**
synthetic dataset (different `NEXO_SYNTHETIC_SEED`) taken end to end: generate →
load → one full orchestrator cycle of the ten agents. It shows the system runs
deterministically and produces a coherent HITL inbox across *varied* data, not a
single hand-picked fixture. No real client data is involved.

{table}

- **acciones** = HITL actions proposed by the ten agents in one cycle.
- **estado** = run status; `ok` means all cross-agent reconciliations held.
- Same seed → identical dataset and identical results (deterministic generator).

Reproduce: `python scripts/run_datasets.py`
"""


def main() -> int:
    results = []
    for seed in SEEDS:
        with tempfile.TemporaryDirectory(prefix=f"nexo-ds-{seed}-") as d:
            print(f"running seed {seed} ...", file=sys.stderr)
            results.append(run_seed(seed, Path(d)))
    md = render_md(results)
    out = REPO_ROOT / "docs" / "EVIDENCE.md"
    out.write_text(md, encoding="utf-8")
    print(f"wrote {out}")
    print(md)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
